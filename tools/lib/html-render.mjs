import { cite, oneLine, docDate, extractSection, firstBullets } from '../handoff.mjs';

/** Escape a string for safe interpolation into HTML text/attribute content. */
export function esc(s) {
  return String(s == null ? '' : s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

