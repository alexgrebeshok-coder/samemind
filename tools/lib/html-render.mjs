// html-render.mjs — self-contained HTML projections for `samemind board --html` and
// `samemind handoff --html` (see gbrain idea-html-projections.md: canon stays markdown,
// HTML is always a generated face, never storage).
//
// This module is a PURE RENDERER: it consumes the same data models board.mjs/handoff.mjs
// already build for the markdown output (`buildBoardModel` / `buildHandoffModel`) — it does
// not re-derive filtering/sorting logic, and it never parses the markdown the other renderer
// produces. One data model, two faces (markdown + HTML).
//
// Hard constraints (see gbrain idea + the naряд that wired this in):
//   - zero external resources — no CDN, no <link>, no remote fonts/images/scripts
//   - zero JavaScript — this is a static report, not an app
//   - light + dark theme via `prefers-color-scheme` (no toggle, no JS)
//   - mobile-friendly: max-width layout, wrapping long titles/paths
//   - a small SVG visualization per page (board: kanban bar chart + Ideas strip;
//     handoff: decision timeline), generated by code, not a charting library
import { pathToId } from './okf.mjs';
